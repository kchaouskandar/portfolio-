// ===== LANGUAGE SWITCHER =====
let currentLang = localStorage.getItem('lang') || 'en';

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('lang', lang);

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    const value = key.split('.').reduce((o, k) => o?.[k], translations[lang]);
    if (value !== undefined) {
      if (el.tagName === 'META') {
        el.setAttribute('content', value);
      } else {
        el.innerHTML = value;
      }
    }
  });

  document.documentElement.lang = lang;

  document.querySelectorAll('.lang-toggle__option').forEach(opt => {
    opt.classList.toggle('active', opt.dataset.lang === lang);
  });
}

// ===== LANG TOGGLE =====
document.getElementById('langToggle')?.addEventListener('click', (e) => {
  const target = e.target.closest('.lang-toggle__option');
  if (target && target.dataset.lang && target.dataset.lang !== currentLang) {
    setLanguage(target.dataset.lang);
  }
});

// Initialise language on load
setLanguage(currentLang);

// ===== MOBILE NAV =====
const navToggle = document.getElementById('navToggle');
const nav = document.getElementById('nav');

navToggle.addEventListener('click', () => {
  nav.classList.toggle('active');
  navToggle.classList.toggle('active');
});

document.querySelectorAll('.nav__link').forEach(link => {
  link.addEventListener('click', () => {
    nav.classList.remove('active');
    navToggle.classList.remove('active');
  });
});

// ===== SCROLL REVEAL =====
const revealObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
      }
    });
  },
  { threshold: 0.1, rootMargin: '0px 0px -40px 0px' }
);

document.querySelectorAll('[data-reveal]').forEach(el => revealObserver.observe(el));

// ===== CONTACT FORM =====
const contactForm = document.getElementById('contactForm');
const formStatus = document.getElementById('formStatus');

if (contactForm) {
  contactForm.addEventListener('submit', async (e) => {
    e.preventDefault();
    const formData = new FormData(contactForm);
    const data = Object.fromEntries(formData);
    formStatus.textContent = '...';
    formStatus.style.color = 'var(--text-secondary)';
    try {
      const response = await fetch(contactForm.action || 'https://formspree.io/f/your-form-id', {
        method: 'POST',
        body: JSON.stringify(data),
        headers: { 'Content-Type': 'application/json', 'Accept': 'application/json' }
      });
      if (response.ok) {
        formStatus.textContent = currentLang === 'fr' ? 'Merci ! Votre message a été envoyé.' : 'Thanks! Your message has been sent.';
        formStatus.style.color = '#d97706';
        contactForm.reset();
      } else {
        formStatus.textContent = currentLang === 'fr' ? 'Une erreur est survenue. Réessayez plus tard.' : 'Something went wrong. Please try again later.';
        formStatus.style.color = '#ef4444';
      }
    } catch {
      formStatus.textContent = currentLang === 'fr' ? 'Une erreur est survenue. Réessayez plus tard.' : 'Something went wrong. Please try again later.';
      formStatus.style.color = '#ef4444';
    }
  });
}

// ===== CLOSE NAV ON RESIZE =====
window.addEventListener('resize', () => {
  if (window.innerWidth > 768) {
    nav.classList.remove('active');
    navToggle.classList.remove('active');
  }
});

// ===== HEADER SCROLL EFFECT =====
const header = document.getElementById('header');
window.addEventListener('scroll', () => {
  header.style.borderBottomColor = window.pageYOffset > 50 ? 'var(--border-hover)' : 'var(--border)';
});

// ===== HERO MOUSE PARALLAX =====
const hero = document.querySelector('.hero');
if (hero) {
  hero.addEventListener('mousemove', (e) => {
    const shapes = hero.querySelectorAll('.hero__shape');
    const x = (e.clientX / window.innerWidth - 0.5) * 2;
    const y = (e.clientY / window.innerHeight - 0.5) * 2;
    shapes.forEach((shape, i) => {
      const factor = (i + 1) * 10;
      shape.style.transform = `translate(${x * factor}px, ${y * factor}px)`;
    });
  });
}
